// IF e else

let nota = 7;

if (nota >= 9) {
    console.log("Excelente")
} else if (nota >= 7) {
    console.log("Aprovado")
} else {
    console.log("Reprovado")
}

// Switch

let cor = "azul";

switch (cor) {
    case "vermelho":
        console.log("Pare!");
        break;
    case "amarelo":
    console.log("Atenção!");
    break;
    case "verde":
    console.log("Siga!");
    break;
    default:
        console.log("Cor inválida");
        break;
}




