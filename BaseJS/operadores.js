// Operadores aritméticos
let a = 10, b = 3;

console.log(a + b);
console.log(a % b);
console.log(a ** b);

// Operadores de Comparação

console.log(5 == "5");
console.log(5 === "5");
console.log(10 >= 7);

// Operadores Lógicos

let idade = 20;
let temCNH = true;

console.log(idade >= 18 && temCNH);
console.log(idade >= 18 || temCNH);
console.log(!temCNH);

